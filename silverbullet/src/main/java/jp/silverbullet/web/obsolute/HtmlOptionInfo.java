package jp.silverbullet.web.obsolute;

public class HtmlOptionInfo {
	private String id;
	private String title;
	
	public HtmlOptionInfo(String id, String title) {
		this.id = id;
		this.title = title;
	}
	
	public String getId() {
		return id;
	}

	public String getTitle() {
		return title;
	}

}
